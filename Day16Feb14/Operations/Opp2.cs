    public partial class Opp2
    {/// <summary>
    /// Multiplaction of Two Numbers
    /// </summary>
    /// <param name="a"></param>
    /// <param name="b"></param>
    /// <returns>Mul of 2 Numbers</returns>
        public int Mul(int a, int b)
        {
            return a * b;
        }
        /// <summary>
        /// Division of Two Numbers
        /// </summary>
        /// <param name="a"></param>
        /// <param name="b"></param>
        /// <returns>Div of 2 Numbers</returns>
        public int Div(int a, int b)
        {
            return a / b;
        }
    }
